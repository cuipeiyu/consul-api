func walkAllFiles(path string) {
	if err := filepath.Walk(path, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		if info.IsDir() {
			return nil
		}
		if filepath.Ext(path) != ".go" {
			return nil
		}
		// Don't extract from test files.
		if strings.HasSuffix(path, "_test.go") {
			return nil
		}

		parseGoFile(path)

		return nil
	}); err != nil {
		return
	}
}

